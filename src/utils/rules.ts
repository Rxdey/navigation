/** 数字两位小数 */
export const numberFix2 = /^(([0-9][0-9]*)|(([0]\.\d{1,2}|[1-9][0-9]*\.\d{1,2})))$/;
/** 数字一位小数 */
export const numberFix1 = /^(([0-9][0-9]*)|(([0]\.\d{1}|[1-9][0-9]*\.\d{1})))$/;
/** 纯数字 */
export const onlyNumber = /^\d{1,}$/;
/** 手机 */
export const mobile = /^[1][3,4,5,6,7,8,9][0-9]{9}$/;
/** 手机和电话 */
export const mobileAndPhone = /^((0\d{2,3}-\d{7,8})|([023456789]\d{6,7})|(1[358479]\d{9}))$/;